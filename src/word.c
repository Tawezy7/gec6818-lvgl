void lcdShowOneWord16x16(int x, int y, unsigned char *word)
{
	// 显示16行
	for ( int i=0; i<16; i++ )
	{
		// 一行解析2个字节
		for ( int j=0; j<2; j++ )
		{
			// 每一个字节解析8个bit
			for ( int k=0; k<8; k++ )
			{
				// bit 为1 的位置画黑色
				// bit 为0 的位置画白色
				if ( word[i*2+j] & (1<<k) )
					lcdDrawPoint(x + 8*j + k, y + i, frontColor);
				else
					lcdDrawPoint(x + 8*j + k, y + i, bgColor);
			}
		}
		
	}
}

void lcdShowOneWordASCII8x16(int x, int y, unsigned char ASCII)
{
	// 显示16行
	for ( int i=0; i<16; i++ )
	{
		// 每一个字节解析8个bit
		for ( int k=0; k<8; k++ )
		{
			// bit 为1 的位置画黑色
			// bit 为0 的位置画白色
			if ( asciiWord[ASCII-32][i] & (1<<k) )
				lcdDrawPoint(x + k, y + i, frontColor);
			else
				lcdDrawPoint(x + k, y + i, bgColor);
		}	
	}
}

void lcdShowString8x16(int x, int y, char *str)
{
	char *temp = str;
	while ( *temp )
	{
		lcdShowOneWordASCII8x16(x + (temp-str)*8 , y, *temp);
		temp++;
	}
}
