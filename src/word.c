void lcdShowOneWord64x64(int x, int y, unsigned char *word)
{
    // 显示16行
    for (int n = 0; n < 8; n++)
    {
        if (n < 4)
        {
            for (int i = 0; i < 64; i++)
            {
                // 一行解析2个字节
                for (int j = 0; j < 4; j++)
                {
                    // 每一个字节解析8个bit
                    for (int k = 0; k < 8; k++)
                    {
                        // bit 为1 的位置画黑色
                        // bit 为0 的位置画白色
                        if (word[i * 4 + j + n * 256] & (1 << k))
                            lcdDrawPoint((x + 8 * j + k + n * 32) % 800, y + i, 0x00732BF5);
                        else
                            lcdDrawPoint((x + 8 * j + k + n * 32) % 800, y + i, WHITE);
                    }
                }
            }
        }
        else
        {
            for (int i = 0; i < 64; i++)
            {
                // 一行解析2个字节
                for (int j = 0; j < 8; j++)
                {
                    // 每一个字节解析8个bit
                    for (int k = 0; k < 8; k++)
                    {
                        // bit 为1 的位置画黑色
                        // bit 为0 的位置画白色
                        if (word[i * 8 + j + (n - 4) * 512 + 4 * 256] & (1 << k))
                            lcdDrawPoint((x + 8 * j + k + (n - 4) * 64 + 4 * 32) % 800, y + i, 0x00732BF5);
                        else
                            lcdDrawPoint((x + 8 * j + k + (n - 4) * 64 + 4 * 32) % 800, y + i, WHITE);
                    }
                }
            }
        }
    }
}

